.MODEL SMALL
.STACK 100H
.DATA
    NL EQU 0DH,0AH
    MSG1 DB NL,"ENTERN THE NUMBER $:"
    MSG2 DB NL,"THE REVERS ORDER $:"
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    
    MOV AH,9
    LEA DX, MSG1
    INT 21H

    MOV AH,1

INPUT:
    INT 21H
    MOV BL,AL
    CMP BL,0DH
    JE NEXT
    PUSH BX
    JMP INPUT

NEXT:
    MOV AH,9
    LEA DX,MSG2
    INT 21H

    MOV AH,2

OUTPUT:
    POP DX
    INT 21H
    CMP SP,0100H
    JNE OUTPUT

EXIT:
    MOV AH,4CH
    INT 21H

MAIN ENDP
END MAIN