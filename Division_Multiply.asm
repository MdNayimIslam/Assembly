.MODEL SMALL
.STACK 100H
.DATA
    NL EQU 0DH,0AH
    MSG1 DB NL,"ENTER TWO NUMBERS $:"
    NUM1 DB ?
    NUM2 DB ?
    MSG2 DB NL,"THE MULTIPLY VALUE IS $:"
    MSG3 DB NL,"THE DIVISION VALUE IS $:"

.CODE

MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    CALL INPUT
    CALL MULTIPLY
    CALL DIVISION

    MOV AH,4CH
    INT 21H

MAIN ENDP

INPUT PROC
    MOV AH,9
    LEA DX,MSG1
    INT 21H

    MOV AH,1
    INT 21H
    MOV NUM1,AL
    SUB NUM1,30H

    INT 21H
    MOV NUM2,AL
    SUB NUM2,30H

    RET 
INPUT ENDP

MULTIPLY PROC
    MOV AL,NUM1
    MUL NUM2
    MOV BL,AL

    MOV AH,9
    LEA DX,MSG2
    INT 21H

    MOV AH,2
    MOV DL,BL
    ADD DL,30H
    INT 21H

    RET 
MULTIPLY ENDP

DIVISION PROC
    MOV AL,NUM1
    CBW
    DIV NUM2
    MOV BL,AL

    MOV AH,9
    LEA DX,MSG3
    INT 21H

    MOV AH,2
    MOV DL,BL
    ADD DL,30H
    INT 21H
    
    RET 
    DIVISION ENDP
END MAIN